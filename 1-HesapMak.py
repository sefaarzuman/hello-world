sayi1 = int(input ("1. sayiyi gir: "))
sayi2 = int(input ("2. sayiyi gir: "))

islem = input ("islemi sec(+, -, *, /):")
if islem == "+":
   print("sonuc:", sayi1 + sayi2)

elif islem == "-":
    print("sonuc:", sayi1 - sayi2)

elif islem == "*":
    print("sonuc:", sayi1 * sayi2)

elif islem == "/":
    print("sonuc:", sayi1 / sayi2)